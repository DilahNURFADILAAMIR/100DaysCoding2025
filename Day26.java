public class Day26 {

        public static void main(String[] args) {
            // Operator Aritmatika
            int a = 10;
            a += 5; // sama dengan a = a + 5
            System.out.println("Aritmatika: a = " + a);

            // Operator Bitwise
            int b = 5;  // biner: 0101
            b &= 3;     // sama dengan b = b & 3
            System.out.println("Bitwise: b = " + b);
        }
}
