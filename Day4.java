public class Day4 {

        public static void main(String[] args) {

            // Contoh penggunaan \n (new line)
            System.out.print("Halo, ini baris pertama.\nIni baris kedua.\nIni baris ketiga.\n");

            // Contoh penggunaan \t (tab)
            System.out.println("Nama\t\t: Dila");
            System.out.println("Pekerjaan\t: Mahasiswi");
            System.out.println("Hobi\t\t: Membaca");

            // Contoh penggunaan \" (double quote)
            System.out.println("Dia berkata: \"Belajar Java itu seru!\"");

            // Contoh penggunaan \' (single quote)
            System.out.println("Karakter tunggal: \'A\'");

            // Contoh penggunaan \\ (backslash)
            System.out.println("Path file: C:\\Users\\Public\\Documents");

            // Contoh penggunaan \b (backspace)
            System.out.println("Halo Dunia!!!\b\b\b"); // 3 kali backspace

            // Contoh penggunaan \r (carriage return)
            System.out.print("Belajar Java\rMulai Lagi");

        }
    }
